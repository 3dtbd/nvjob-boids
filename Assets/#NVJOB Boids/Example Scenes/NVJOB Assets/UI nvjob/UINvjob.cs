using UnityEngine;
using UnityEngine.UI;


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


public class UINvjob : MonoBehaviour
{
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Awake()
    {
        //---------------------------------

        transform.Find("Panel/logo").GetComponent<Button>().onClick.AddListener(Logo);
        transform.Find("Panel/support").GetComponent<Button>().onClick.AddListener(Support);
        transform.Find("Panel/patrons").GetComponent<Button>().onClick.AddListener(Patrons);

        //---------------------------------
    }


    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Logo() { Application.OpenURL("https://nvjob.github.io/unity/nvjob-boids"); }
    void Support() { Application.OpenURL("https://nvjob.github.io/support"); }
    void Patrons() { Application.OpenURL("https://nvjob.github.io/patrons"); }


    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
